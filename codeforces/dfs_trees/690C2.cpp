int dfs(int node, int par = -1){
    vi depths;
    for(auto ad: adj[node]){
        if(ad==par) continue;
        depths.push_back(dfs(ad, node));
    }
    int m1 = 0, m2 = 0;
    sort(all(depths));
    if(depths.size())   
        m1 = depths.back(), depths.pop_back();
    if(depths.size())
        m2 = depths.back(), depths.pop_back();
    
    mx = max(mx, m1+m2);

    return 1 + m1;
}

