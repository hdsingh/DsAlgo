// Collect odd pos, even pos and sort
// Then either even+odd or odd+even
int main(){
    int t; string s;
    cin>>t;
    while(t--){
        cin>>s;
        string odd, even;
        for(auto c: s){
            if(c&1)
                odd+=c;
            else even+=c;
        }
        
        sort(all(odd)); sort(all(even));
        // print(odd); print(even);
        string oe = odd+even, eo =even+odd;
        string ans;

        if(check(oe)){
            ans = oe;
        }else if(check(eo))
            ans = eo;
        else 
            ans = "No answer";
        cout<<ans<<endl;
    }
    return 0;
}