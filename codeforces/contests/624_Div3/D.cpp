int main(){
    int a,b,c,t,A,B,C,curDif,minDif;
    cin>>t;
    while(t--){
        cin>>a>>b>>c;
        minDif = INT_MAX;
        for(int ai=1; ai<=2*a; ++ai){
            for(int bi=ai; bi<=2*b; bi+=ai){
                for(int i=0; i<2; ++i){
                    int ci = (c/bi) * bi + i * bi; // nearest to c, (c/bi) * bi + (0, or bi) (loer and upper)
                    curDif = abs(a-ai) + abs(b-bi) + abs(c-ci);
                    if(curDif<minDif){
                        minDif = curDif;
                        A = ai, B = bi, C = ci;
                    }
                }
            }
        }
        cout<<minDif<<endl;
        cout<<A<<" "<<B<<" "<<C<<endl;
    }
    return 0;
}