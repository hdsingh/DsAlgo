class Solution {
    vi fact;
public:
    string getPermutation(int n, int k) {
        fact.resize(10);
        fact[0] = 1;
        for(int i=1; i<10; ++i) fact[i] = fact[i-1]*i;
        vi nums;
        for(int i=1; i<=n; ++i) nums.push_back(i);
        vi seq;
        getSeq(n,k, seq);

        string out;
        for(auto ord: seq){
            int el = nums[ord-1];
            out+=(el+'0');
            nums.erase(nums.begin()+ord-1);
        }

        return out;
    }

    void getSeq(int n, int k, vi &a){
        if(n==0) return;
        int now = (k + fact[n-1] - 1)/fact[n-1];
        a.push_back(now);
        k = k - (now-1)*fact[n-1];
        getSeq(n-1,k, a);
    }

};

