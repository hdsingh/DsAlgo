class Solution{
public:
  bool search(vi a, int x) {
		int n = a.size();
		int l = 0; int r = n-1;
		int mid=0;
		while(l<r){
			mid = l + (r-l)/2;
			if(a[mid]==x){
				return true;
			}
			// not rotated
			else if(a[mid]<a[r]){
				// locate the pos of x
				if(a[mid]<=x && x<=a[r])
					l = mid+1;
				else 
					r = mid-1;
			}
			// rotated
			else if(a[mid]>a[r]){
				if(a[l]<= x && x<=a[mid])
					r = mid-1;
				else 
					l = mid+1;
			}
			else {
				--r;
			}
		}

		return a[l]==x;
  }

};

