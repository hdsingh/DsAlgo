class Solution {
public:
    bool validateStackSequences(vector<int>& pushed, vector<int>& popped) {
        int n = pushed.size();
        stack<int> stk;
        int j = 0;
        for(int i=0; i<n; i++){
            stk.push(pushed[i]);
            while(stk.size() && popped[j]==stk.top()){
                stk.pop(); ++j;
            }
        }
        return stk.empty();    
    }
};
