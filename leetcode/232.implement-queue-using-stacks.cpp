// Movement of element happens, only once;
// out is used for peek/pop, in is used for pushing,
// when out becomes empty, elelmts of in are moved into in
// thus giving O(1) amortised time complexity
class MyQueue {
    stack<int> in, out;
public:
    /** Initialize your data structure here. */
    MyQueue() {
        
    }
    
    /** Push element x to the back of queue. */
    void push(int x) {
        in.push(x);
    }
    
    /** Removes the element from in front of queue and returns that element. */
    int pop() {
        peek();
        int top = out.top();
        out.pop();
        return top;
    }
    
    /** Get the front element. */
    int peek() {
        if(out.empty())
            while(in.size())
                out.push(in.top()), in.pop();
        return out.top();
    }
    
    /** Returns whether the queue is empty. */
    bool empty() {
        return in.empty() && out.empty();
    }
};
