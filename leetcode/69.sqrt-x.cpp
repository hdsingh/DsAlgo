class Solution {
public:
    int mySqrt(int x) {
        int l(0), r(x), root;
        while(l<=r){
            int m = l + (r-l)/2;
            ll prod = m*1ll*m;
            if(prod==x)
                return m;
            else if(prod<x){
                // Save here because we need floor
                root = m;
                l = m + 1;
            }else 
                r = m - 1;
        }
        return root;
    }
};
