class Solution {
public:
    int findMinFibonacciNumbers(int k) {
        vi fibs = {0,1};
        int n = 2;
        while(true){
            int nx = fibs[n-1] + fibs[n-2];
            fibs.push_back(nx);
            ++n;
            if(nx>k) break;
        }   
        
        int cnt = 0;
        while(k>0){
            // find the min element less than k
            auto pos = upper_bound(fibs.begin(), fibs.end(), k);
            --pos;
            // deb(*pos);
            k-=*pos;
            ++cnt;
        }

        return cnt;
    }
};

