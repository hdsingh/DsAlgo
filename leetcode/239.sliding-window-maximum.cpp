class Solution0 {
public:
    vector<int> maxSlidingWindow(vector<int>& a, int k) {
        priority_queue<pair<int,int>> pq; // {val, index}
        int n = a.size();

        for(int i=0; i<k-1; ++i)
            pq.push({a[i],i});
    
        vector<int> out;
        for(int i=k-1; i<n; ++i){
            while(pq.size() && i - pq.top().second>=k)
                pq.pop();

            pq.push({a[i],i});
            out.push_back(pq.top().first);
        }
    
        return out;
    }
};

// save in decreasing order (non-inc)
class Solution {
public:
    vector<int> maxSlidingWindow(vector<int>& a, int k) {
        int n = a.size();
        deque<int> dq;
        vector<int> ans;

        for(int i=0; i<n; ++i){
            // remove stale element 
            while(!dq.empty() && i - dq.front()>=k)
                dq.pop_front();

            while(!dq.empty() && a[dq.back()]<a[i])
                dq.pop_back();
            
            dq.push_back(i);
            if (i>=k-1) ans.push_back(a[dq.front()]);
        }

        return ans;
    }
};

