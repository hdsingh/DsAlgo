// Total hamming distance for the i-th bit =
// (the number of zeros in the i-th position) * (the number of ones in the i-th position)
class Solution {
public:
    int totalHammingDistance(vector<int>& a) {
        int n = a.size();
        
        // int out = 0;
        // fore(i, 0, n){
        //     fore(j, i+1, n){
        //         out+=__builtin_popcount(a[i]^a[j]);
        //     }
        // }

        int out = 0;
        for(int bit=0; bit<32; bit++){
            int ones(0);
            for(int i=0; i<n; i++){
                if(a[i] & (1<<bit))
                    ++ones;

            }
            out+=(ones*(n-ones));
        }

        return out;
    }
};
