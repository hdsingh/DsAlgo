class CombinationIterator {
    int n, len, cur;
    string s;
public:
    CombinationIterator(string s, int len) {
        n = s.size();
        this->s = s; 
        this->len = len;
        cur = (1<<n)-1;
    }

    string next() {
        if(!hasNext()) return "";
        string out;
        for(int i=0; i<n; ++i){
            if((cur>>(n-i-1))&1)
                out+=s[i];
        }       
        --cur;
        return out;
    }
    
    bool hasNext() {
        while(cur>0 && __builtin_popcount(cur)!=len)
            --cur;
    
        return (cur!=0);
    }
};

