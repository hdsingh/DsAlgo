class Solution {
public:
    int thirdMax(vector<int>& a) {
        int n = a.size();
        set<int> s;

        for(int &x: a){
            s.insert(x);
            if(s.size()>3)
                s.erase(s.begin());
        }
        return s.size()==3 ? *s.begin() : *s.rbegin();
    }
};
