// Refenced:
// For example, for number 26 to 30
// Their binary form are:
// 11010
// 11011
// 11100　　
// 11101　　
// 11110
class Solution {
public:
    int rangeBitwiseAnd(int l, int r) {
        // whar matters is, the common bits at left which are one throughout the range
        // so count the uncommon bits, and then relpace them with 0s
        int cnt = 0 ;
        while(l!=r){
            l>>=1;
            r>>=1;
            cnt++;
        }
        return l<<cnt;
    }
};

