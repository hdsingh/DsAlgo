class Solution {
public:
    string removeKdigits(string a, int k) {
        int n = a.size();
        string res;
        int keep = n - k;
        // whenever we meet a digit lesser than prev, discard the prev
        for(auto c: a){
            while(k && res.size() && res.back()>c){
                res.pop_back();
                --k;
            }
            res.push_back(c);
        }
        res.resize(keep);
        while(!res.empty() && res[0]=='0') res.erase(res.begin());
        return res.empty() ? "0" : res;
    }
};
