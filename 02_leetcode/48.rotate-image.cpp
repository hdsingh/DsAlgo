// Revere + swap symmetrically
//   [1,2,3],
//   [4,5,6],
//   [7,8,9]
//     |
//   [7,8,9]
//   [4,5,6]
//   [1,2,3]
//     |
//   [7,4,1]
//   [8,5,2]
//   [9,6,3]
class Solution {
public:
    void rotate(vector<vector<int>>& img) {
        reverse(all(img));
        int n = img.size();
        for(int i=0; i<n; i++){
            for(int j=i+1; j<n; j++){
                swap(img[i][j], img[j][i]);
            }
        }
    }
};
