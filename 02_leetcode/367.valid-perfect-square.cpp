class Solution {
public:
    bool isPerfectSquare(int n) {
        double l=0; double r = n; 
        double eps = 0.01;
        while(r-l>eps){
            double mid = l + (r-l)/2;
            if(mid*mid<n)
                l = mid;
            else 
                r = mid;
        }
        double rt = l + (r-l)/2;
        // deb(rt);
        // deb(round(rt))
        return ( abs(round(rt)-rt)  < eps ? true : false);
    }
};

