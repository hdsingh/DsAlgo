class Solution {
public:
    int maxProfit(vector<int>& p) {
        int n = p.size();
        if(!n) return 0;
        
        int cash = 0;
        int hold = -p[0];
        // vi ca, ha;
        // ca.pb(cash); ha.pb(hold);

        for(int i=1; i<n; i++){
            cash = max(cash, hold + p[i]);
            hold = max(hold, cash - p[i]);
            // ca.pb(cash); ha.pb(hold);
        }

        // print(p);
        // print(ca);
        // print(ha);

        return cash;
    }
};
