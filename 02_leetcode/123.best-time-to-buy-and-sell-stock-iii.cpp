class Solution {
public:
    int maxProfit(vector<int>& p) {
        int n = p.size();
        int k=2;
        vvi buy(k+1, vi(n+1));
        vvi sell(k+1, vi(n+1));
        for(int i=1; i<=k; i++)
            buy[i][0] = INT_MIN;

        for(int i=1; i<=n; i++){
            for(int j=1; j<=k; j++){
                sell[j][i] = max(sell[j][i-1], buy[j][i-1] + p[i-1]);
                buy[j][i] = max(buy[j][i-1], sell[j-1][i-1] - p[i-1]);

            }
        }
        return sell[k][n];
    }
};
