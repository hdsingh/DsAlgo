class Solution {
public:
    int scoreOfParentheses(string s) {
        int n =  s.size();
        int depth = 0;
        int tot = 0;
        char pre = '0';

        for(auto cur: s){
            depth += cur=='(' ? 1 : -1;
            if(pre=='(' && cur==')')
                tot+= 1<<depth;
            pre = cur;
        }

        return tot;
    }
};
