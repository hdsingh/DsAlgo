// if the sum(ai+al+ar) is less than target, then we need to increase it
// Since i is fixed, we can only move l and r
// In order to increase the sum we should move l, and
// in order to decrease it r should be moved
int main(){
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    ll n,X; cin>>n>>X;
    vector<pll> a(n);
    forn(i,n){
        cin>>a[i].x;
        a[i].y = i+1;
    }

    sort(all(a));
    forn(i,n){
        if(i && a[i]==a[i-1]) continue;
        int l = i+1, r = n-1;
        while(l<r){
            ll sum = a[i].x + a[l].x + a[r].x;
            if(sum==X){
                cout<<a[i].y<<" "<<a[l].y<<" "<<a[r].y<<"\n";
                return 0;
            }else if(sum<X) ++l;
            else if(sum>X) --r;
        }
    }
    cout<<"IMPOSSIBLE\n";
    return 0;
}